export const STATES_BY_COUNTRY: Record<CountryCode, readonly RegionOption[]> = {
  US: US_STATES,
  IN: IN_STATES,
  CA: CA_PROVINCES,
};

export const DEFAULT_COUNTRY: CountryCode = 'US';

export function getStatesForCountry(country: string): readonly RegionOption[] {
  return STATES_BY_COUNTRY[country as CountryCode] ?? US_STATES;
}

/** Include a legacy/custom region when it is not in the canonical list for the country. */
export function regionSelectOptions(
  regions: readonly RegionOption[],
  current?: string,
): RegionOption[] {
  const trimmed = current?.trim();
  if (trimmed && !regions.some((r) => r.code === trimmed)) {
    return [{ code: trimmed, name: trimmed }, ...regions];
  }
  return [...regions];
}

export function isValidRegionForCountry(country: string, state: string): boolean {
  if (!state.trim()) return true;
  return getStatesForCountry(country).some((r) => r.code === state);
}
